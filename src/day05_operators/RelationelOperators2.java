package day05_operators;

public class RelationelOperators2 {
    public static void main(String[] args) {

        // ==:
        System.out.println(10 == 20);
        System.out.println("ulker" == "ulker");
        System.out.println("----------------------");



        System.out.println(1000 == 2000);

        System.out.println("Java" == "Java");

        System.out.println("java" == "JAVA");

        System.out.println("Muhtar" == "Good Guy");

        System.out.println( true == false);

        System.out.println("-----------------------------");

        // != :
        System.out.println(200 != 200);
        System.out.println(200 !=250);
        System.out.println("-----------------");

        System.out.println(1000 != 2000); // true

        System.out.println("Java" != "Java"); // false

        System.out.println("java" != "JAVA");  // true

        System.out.println("Muhtar" != "Good Guy"); // true

        System.out.println( true != false);  // true



    }

}
